	public class state
	{	
	    int x, y, step;
	    public state(int x1, int y1, int st)
	    {
	    	x= x1; 
	    	y = y1;
	    	step = st;
	    }
	 }   
	 
	public String ableToGet(int x, int y)
	{
		Queue<state> qu = new LinkedList<state>();
		state st = new state(0,0,0);
		qu.offer(st);
		
		while (!qu.isEmpty()) {
			st = qu.poll();
			
			int x1 = st.x;
			int y1 = st.y;
			int step = st.step;
			if (x1 == x && y1 == y)
				return "Possible";
			
			int nxt = (int)Math.pow(3, step);
			if (x1 + nxt <= x) {
				state ns = new state(x1+nxt, y1, step+1);
				qu.offer(ns);
			}
			if (y1 + nxt <= y) {
				state ns = new state(x1, y1+nxt, step+1);
				qu.offer(ns);
			}		
		}
		return "Impossible";
		
	}
	
