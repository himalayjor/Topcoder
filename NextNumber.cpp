class NextNumber {
public:
	int getNextNumber(int);
};

int NextNumber::getNextNumber(int N) {
	
	vector<int> bits(32,0);
    for (int i=0; i<32; i++)
      if (N & (1<<i))
        bits[31-i] = 1;

    // find the next permutation of this many 0s and 1s
    next_permutation( bits.begin(), bits.end() );

    // convert the bits back into a number
    int res = 0;
    for (int i=0; i<32; i++)
      if (bits[i])
        res += 1<<(31-i);
    return res;
}

