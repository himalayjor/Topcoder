	int mod = 1000000007;
	int dp[];
	
	public int theMinCars(int h)
	{
		dp = new int[h+1];
		
		if (h == 0)
			return 1;
		dp[0] = 1;	
		
		for (int i = 1; i <= h; i++) {
			if ( (i % 2) == 0)
				dp[i] = (2*dp[i-1] + 1) % mod;
			else 
				dp[i] = (2*dp[i-1] -1) % mod;	
		}	
		return 	dp[h];
	}
	
