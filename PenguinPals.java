	public int findMaximumMatching(String colors)
	{
		boolean used[] = new boolean[colors.length()];
		int n = colors.length();
		int ans = 0;
		
		if (colors.length() == 1)
			return 0;
			
		for (int i = 0; i < n-1; i++) {
			if (!used[i] && !used[i+1] && colors.charAt(i) == colors.charAt(i+1)) {
				used[i] = used[i+1] = true;
				ans++;
			} 
		}
		if (!used[0] && !used[n-1] && colors.charAt(0) == colors.charAt(n-1)) {
			ans++;
			used[0] = used[n-1] = true;
		}
		StringBuilder sb = new StringBuilder();
		for (int i = 0; i < n; i++) {
			if (!used[i])
				sb.append(colors.charAt(i));
		}
		String S = sb.toString();
		if (ans == 0) {
			int n1 = S.length();
			return (n1-2)/2;
		}
		if (S.length() > 0 && ans > 0)
			return ans + findMaximumMatching(S);
			
		return ans;
	}
	
