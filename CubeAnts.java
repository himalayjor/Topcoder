	public int getMinimumSteps(int[] pos)
	{
		for (int i = 0; i < pos.length; i++) {
			if (pos[i] == 6)
				return 3;
				
		}
		for (int i = 0; i < pos.length; i++) {
			if (pos[i] == 7 || pos[i] == 5 || pos[i] == 2)
				return 2;
		}
		for (int i = 0; i < pos.length; i++) {
			if (pos[i] == 1 || pos[i] == 4 || pos[i] == 3)
				return 1;
		}
		return 0;
	}
	
