	public int minBoxes(int C, int X, int[] high)
	{
		Arrays.sort(high);
		int n = high.length;
		
		int sum = C - X;
		
		int tmp = 0;
		
		int ans = 0;
		for (int i = 0; tmp < sum && i < n; i++) {
			tmp += high[i];
			ans++;
		}
		
		if (tmp > sum)
			ans--;
			
		return high.length - ans;
	}
	
