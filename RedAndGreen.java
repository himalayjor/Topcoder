	public int minPaints(String row)
	{
		int n = row.length();
		int ans = Integer.MAX_VALUE;
		
		for (int r = 0; r <= n; r++) {
			
			int tmp = 0;
			for (int i = 0; i < n; i++) {
				if (i < r && row.charAt(i) == 'G')
					tmp++;
				else if (i >= r && row.charAt(i) == 'R')
					tmp++;	
			}
			ans = Math.min(ans, tmp);
		} 
		 
		return ans;
	}
	
